	<h1>About Motifle</h1>
	<p>
		Motifle is a musical guessing game, where players try to identify a song's referenced melodies. Drawing inspiration from the vast world of <a href="https://hsmusic.wiki">Homestuck music and fanmusic</a>, this game is designed to challenge and entertain fans by testing their knowledge of the many melodies woven into the songs.
		<br><sub>(I also made <a href="https://heardle.homestuck.net">Homestuck Heardle</a>, a game about guessing songs from their beginnings)</sub>
	</p>

